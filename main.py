def add(x,y):
  sum = int(x) + int(y)
  return sum

def subtraction(x,y):
  sum = int(x) - int(y)
  return sum

def multiplication(x,y):
  sum = int(x) * int(y)
  return sum

def division(x,y):
  sum = int(x) / int(y)
  return sum

def modulus(x,y):
  sum = int(x) % int(y)
  return sum
